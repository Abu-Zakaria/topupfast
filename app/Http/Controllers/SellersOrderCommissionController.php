     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $sellers = User::where('is_admin', 2);
        $sellers_temp = User::where('is_admin', 2);

        $total_wallet_balance = $sellers_temp->get()->map(function($seller)
            {
                return $seller->ordersWallet;
            });

        $total_wallet_balance = $total_wallet_balance->sum();

        return inertia()->render('SellersOrderCommission/Index', [
            'sellers' => $sellers->paginate(),
            'total_wallet_balance' => $total_wallet_balance,
        ]);
    }

