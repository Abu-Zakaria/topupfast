     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $orders = Order::where('accept_id', auth()->user()->id)
                        ->orderByRaw("CASE status
                            WHEN \"pending\" THEN 1
                            WHEN \"complete\" THEN 2
                            WHEN \"cancel\" THEN 3
                            END
                        ")
                        ->get();

        return Inertia::render('Seller/MyOrders/Index', [
            'orders' => $orders,
        ]);
    }

